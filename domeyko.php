			<div id="titulo" style="text-align: center">
				<h2>IGNACIO DOMEYKO A. (1802 - 1889)</h1>
				<h3>Precursor de la Enseñanza Minera en Chile </h2>
			</div>
			<img src="img/domeyko.jpg" width="40%">
		
				<p><b>1802 –</b> Nació en el ducado de Lituania, unido a Polonia en el siglo XV, formando el reino unido polaco - lituano.</p>

				<p><b>1820 –</b> Licenciado en Ciencias de la Universidad de Vilna, capital de Lituania.</p>

				<p><b>1823 –</b> Participa en sociedades patrióticas polacas, siendo perseguido por los rusos imperiales que tenían invadida Polonia.</p>

				<p><b>1830 –</b> Revolución contra los rusos. Combate activamente en la lucha patriota por la independencia. Cae Varsovia. Exilio a Francia. </p>

				<p><b>1832 –</b> Exiliado en parís. La colonia polaca mantiene vivo el amor a Polonia. 
				Se interesa por la naciente ciencia de la Geología.</p>

				<p><b>1836 –</b> Estudia en la escuela de Minas de París donde se titula de ingeniero de Minas. Trabaja en yacimientos de hierro en Alsacia.</p>

				<p><b>1837 –</b> Es contratado en Francia por el encargo del Gobierno de Chile, Intendencia de Coquimbo, en un viaje desde Chile del Industrial minero Carlos Lambert, para la cátedra de Química y Mineralogía en el Instituto de Coquimbo (actual Liceo de Hombres de La Serena). </p>

				<p><b>1838 –</b> Viajan desde Francia a Chile, cruzan la Cordillera de los Andes a mula y llegan directamente a La Serena. Inicia sus clases de minería, como enseñanza profesional. Efectúa estudios geológicos en la mina de plata de Arqueros, valle de Elqui.</p>

				<p><b>1840 –</b> Viaje a Copiapó. Estudia las minas de cobre de Carrizal y las de plata de Chañarcillo. Recorre la cordillera de los Andes. Descubre nuevas especies minerales chilenas, la arquerita, la algodonita y la domeykita, bautizadas con nombres locales. Informes publicados en los Anales de Minas, en Francia.</p>

				<p><b>1842 –</b> A Santiago por primera vez. Consigue becas para los mejores tres alumnos serenenses egresados, que van a estudiar a Francia. Estudia el yacimiento “El Teniente”, frente a Rancagua.</p>

				<p><b>1844 –</b> Publica en La Serena, para Chile, los libros “Tratado de Ensayes” (análisis químicos) y su famosa “Mineralogía”, que se usan en toda América.</p>

				<p><b>1845 –</b> Viaja a la Araucanía, sur de Chile. Estudia las minas de carbón y al pueblo araucano.</p>

				<p><b>1846 –</b> Completa su contrato de enseñanza en La Serena, Crea su colección mineralógica (base del Museo). Entrega su cátedra a los alumnos becados que habían egresado. Trata de volver a Francia para luchar por Polonia. </p>

				<p><b>1847 –</b> Autoridades educacionales universitarias chilenas solicitan sus servicios. Es nombrado miembro del Consejo de la Universidad de Chile (fundada en 1843). Reforma la educación superior.</p>

				<p><b>1848 –</b> El Gobierno de Chile le concede la ciudadanía chilena, por los servicios al país, en la enseñanza pública y en la investigación científica.</p>

				<p><b>1850 –</b> Se casa con doña Enriqueta Sotomayor y Guzmán. Tuvo 4 hijos.</p>

				<p><b>1850 –</b> Es nombrado perito en juicios legales en las minas de plata de Chañarcillo, Copiapó.</p>

				<p><b>1852 –</b> Por sus Investigaciones mineralógicas recibe desde Polonia el grado de Doctor de la Universidad de Cracovia.</p> 

				<p><b>1867 –</b> Después de 17 años de docencia, investigación y publicaciones geológicas, es nombrado Rector de la Universidad de Chile, cargo que desempeño tres veces hasta 1882, renunciando por edad en 1883 después de 16 años. Desarrollo las Ciencias, las Artes, la Arquitectura y la Pedagogía. Además continuó recibiendo a los egresados de los Liceos de La Serena y de Copiapó, que se titulaban de ingenieros de mina. Se mantenía así la continuidad de enseñanza minera en La Serena, iniciada por él, la más antigua de Chile. </p>

				<p><b>1883 –</b> Es nombrado Miembro Honorario de la Sociedad Nacional de Minería, fundada ese mismo año como la agrupación de industriales de la actividad minera chilena.</p>

				<p><b>1884 –</b> Viaja a Europa con sus hijos, para cumplir su sueño de volver a Polonia después de 52 años de exilio. Regresa a París, Austria y Polonia, en Cracovia y Varsovia, que aún continuaban bajo dominio ruso. Visita Tierra santa y Turquía.</p>
				<p><b>1888 –</b> Regresa a Chile. Prepara y cataloga la Colección Mineralógica, presentada por Chile en la Exposición Universal de París de 1889, encargada por la Soc.Nac. de Minería, su último trabajo.</p>

				<p><b>1889 –</b> Fallece en Santiago. Sus descendientes viven en Chile. Su obra se basó en la fuerza del amor a su patria, Polonia, a Dios, a la educación y a la ciencia, y al país que le dio paz: CHILE.</p>
